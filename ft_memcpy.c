#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	if (!dst && !src)
		return (NULL);
	while (n--)
		((t_u8p)dst)[n] = ((t_u8p)src)[n];
	return (dst);
}
