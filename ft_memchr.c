#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
		if (U8P(s)[i] == (unsigned char)c)
			return (&U8P(s)[i]);
	return (0);
}
